/// 品詞が内容語（助詞・助動詞・記号・未知語でない）か判定する
pub fn is_content_pos(pos: &str) -> bool {
    !(pos.starts_with("助詞")
        || pos.starts_with("助動詞")
        || pos.starts_with("記号")
        || pos.starts_with("未知語")
        || pos.starts_with("フィラー")
        || pos.starts_with("カタカナ"))
}

